
public class invertedhalfwithnumbers {
    public static void main(String args[]) {
    
    
        int n = 5;

      //outer lop
    for (int i=1; i<=n; i++){

        //inner loop
        for (int j=1; j<=n-1+1; j++){
            System.out.print(j);
        }
        System.out.println();
    }

  }
        
    
}
